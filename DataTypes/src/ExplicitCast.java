
public class ExplicitCast {

	public static void main(String[] args) {

		int i = 100;
		byte b = (byte) i;
		int x = 97;
		char ch = (char) x;
		System.out.println(b);
		System.out.println(ch);
		int y = 130;
		byte z = (byte) y;
		System.out.println(z);
		int j = 612;
		byte k = (byte) j;
		char ch1 = (char) k;
		System.out.println(ch1);
	}

}
